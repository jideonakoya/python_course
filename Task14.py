# Declare a string of any value and print out the string in upper case

word = "beginning of the talk"

# Upper
upper_value = word.upper()
print("Upper=", word.upper())

# capitalize
capitalized_value = word.capitalize()
print("Capitalized=", word.capitalize())

#index
for_index = word.index("n")
print("Index=", for_index)

# find ->
for_position = word.find("i")
print("Find=", for_position)